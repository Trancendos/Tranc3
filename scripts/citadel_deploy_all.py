def _pre_deploy_gate() -> None:
    _section("Pre-deploy quality gate")
    _run([_python(), "scripts/pre_deploy_quality_gate.py"])


def _preflight() -> None:
    _section("Citadel preflight")
    _run([_python(), "scripts/citadel_compose_validate.py"])
    code = _run([_python(), "scripts/citadel_preflight.py"], check=False)
    if code != 0:
        _log("  WARN: preflight reported issues — continuing for bootstrap")


def _docker_cmd() -> list[str]:
    if shutil.which("docker"):
        return ["docker", "compose"]
    raise SystemExit(
        "ERROR: Docker not found. Install Docker Desktop (Windows/Mac) or Docker Engine (Linux)."
    )


def _compose(args: list[str], *, check: bool = True) -> int:
    return _run([*_docker_cmd(), "-f", str(COMPOSE_FILE), *args], check=check)


def _deploy_docker(*, profile: str, skip_build: bool) -> None:
    _section("Docker deploy (Citadel stack)")
    services = list(CORE_SERVICES)
    if profile == "full":
        services.extend(FULL_EXTRA)

    if not skip_build:
        _compose(["build", *services])

    _compose(["up", "-d", "valkey", "vault", "traefik", "ollama"])
    time.sleep(5)
    _log("  If Vault is new, run: deploy\\vault\\init-citadel.sh (Git Bash) or WSL")

    _compose(["up", "-d", *services])

    _section("Wait for health")
    code = _run(
        [_python(), "scripts/wait_for_healthy.py", "--timeout", "600"],
        check=False,
    )
    if code != 0:
        _log("  WARN: some services still starting — run monitor step below")


def _post_deploy() -> None:
    _section("Post-deploy checks")
    _run([_python(), "scripts/health_check.py", "--json"], check=False)
    _run([_python(), "scripts/zero_cost_audit.py"], check=False)
    _run([_python(), "scripts/production_readiness_score.py"], check=False)
    if (ROOT / "scripts/seed_uat_data.py").is_file():
        _run([_python(), "scripts/seed_uat_data.py"], check=False)


def _monitor() -> None:
    _section("P0/P1 health monitor")
    _run([_python(), "scripts/monitor_p0_workers.py"], check=False)


def _print_urls() -> None:
    _log("")
    _log("Done.")
    _log("  API:       http://localhost:8000/health")
    _log("  Ready:     http://localhost:8000/ready")
    _log("  Gateway:   http://localhost:8003/")
    _log("  Admin OS:  http://localhost:8000/dashboard/infinity-admin-os.html")
    _log("  Grafana:   http://localhost:3000")
    _log("  Traefik:   http://localhost:8888")


def main() -> int:
    parser = argparse.ArgumentParser(description="Tranc3 Citadel — gate + deploy in one script")
    parser.add_argument(
        "--gate-only",
        action="store_true",
        help="Run pre-deploy gate only (no Docker)",
    )
    parser.add_argument(
        "--skip-docker",
        action="store_true",
        help="Gate + env + preflight, but do not start Docker",
    )
    parser.add_argument(
        "--local",
        action="store_true",
        help="Force Citadel Docker deploy (LOCAL_ONLY / migration)",
    )
    parser.add_argument(
        "--cloud-only",
        action="store_true",
        help="Explicit CLOUD_ONLY: gate only, no Docker (default when mode is CLOUD_ONLY)",
    )
    parser.add_argument(
        "--full",
        action="store_true",
        help="Deploy full profile (extra P2 workers)",
    )
    parser.add_argument(
        "--skip-build",
        action="store_true",
        help="Skip docker compose build",
    )
    parser.add_argument(
        "--force-env",
        action="store_true",
        help="Regenerate .env.production",
    )
    parser.add_argument(
        "--install-deps",
        action="store_true",
        help="pip install requirements + ruff/bandit/pytest/pip-audit",
    )
    parser.add_argument(
        "--no-monitor",
        action="store_true",
        help="Skip final make monitor step",
    )
    args = parser.parse_args()

    _ensure_repo()
    mode = _infra_mode()
    _log(f"Tranc3 Citadel deploy — root: {ROOT} — PLATFORM_INFRA_MODE={mode}")

    if args.install_deps:
        _install_deps()

    _pre_deploy_gate()

    if args.gate_only or args.cloud_only:
        _log("")
        _log("Gate-only mode complete (PASS).")
        if mode == "CLOUD_ONLY" or args.cloud_only:
            _log("  CLOUD_ONLY: cloud auto-rotation runs via API/Fly/CF — no Citadel Docker required.")
            _log("  When ready for local: PLATFORM_INFRA_MODE=LOCAL_ONLY scripts/citadel_deploy_all.py --local")
        return 0

    local_deploy = args.local or args.skip_docker is False and mode in (
        "LOCAL_ONLY",
        "HYBRID",
    ) and (
        mode == "LOCAL_ONLY"
        or os.environ.get("CITADEL_LOCAL_STACK", "").lower() in ("1", "true", "yes")
    )
    if mode == "CLOUD_ONLY" and not args.local:
        local_deploy = False

    _generate_env(force=args.force_env, local_stack=local_deploy)
    if not local_deploy:
        _log("")
        _log(f"CLOUD_ONLY ({mode}): skipping Citadel Docker — use --local when your server is ready.")
        _log("  Adaptive: GET /adaptive/mode and GET /adaptive/status on your running API.")
        return 0

    _preflight()

    if args.skip_docker:
        _log("")
        _log("Skip-docker mode complete. Start stack manually when ready:")
        _log("  docker compose -f docker-compose.production.yml up -d")
        return 0

    profile = "full" if args.full else "core"
    _deploy_docker(profile=profile, skip_build=args.skip_build)
    _post_deploy()

    if not args.no_monitor:
        _monitor()

    _print_urls()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
