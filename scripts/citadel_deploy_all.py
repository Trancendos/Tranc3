#!/usr/bin/env python3
"""
Citadel — one script for pre-deploy gate + optional local Docker deploy.

Default (PLATFORM_INFRA_MODE=CLOUD_ONLY): quality gate only — no Docker.
Use --local or set PLATFORM_INFRA_MODE=LOCAL_ONLY when your Citadel server is ready.

Usage:
  python scripts/citadel_deploy_all.py              # gate; skip Docker if CLOUD_ONLY
  python scripts/citadel_deploy_all.py --gate-only  # quality gate only
  python scripts/citadel_deploy_all.py --local      # force Citadel Docker deploy
  python scripts/citadel_deploy_all.py --install-deps
  python scripts/citadel_deploy_all.py --full --force-env --local

Windows CMD (from repo root):
  py -3.11 scripts\\citadel_deploy_all.py
  scripts\\citadel_deploy_all.bat
"""

from __future__ import annotations

import argparse
import os
import secrets
import shutil
import subprocess
import sys
import time
from datetime import datetime, timezone
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
COMPOSE_FILE = ROOT / "docker-compose.production.yml"

CORE_SERVICES = [
    "traefik",
    "valkey",
    "vault",
    "ollama",
    "tranc3-backend",
    "tranc3-ai",
    "infinity-void",
    "infinity-ws",
    "infinity-auth",
    "users-service",
    "products-service",
    "orders-service",
    "payments-service",
    "api-gateway",
    "monitoring",
    "notifications",
    "infinity-ai",
    "infinity-admin",
    "swarm-coordinator",
    "prometheus",
    "grafana",
]

FULL_EXTRA = [
    "the-grid",
    "files-service",
    "identity-service",
    "health-aggregator",
    "vault-service",
    "gbrain-bridge",
]

PIP_TOOLS = ["ruff", "bandit", "pip-audit", "pytest"]


def _log(msg: str) -> None:
    print(msg, flush=True)


def _section(title: str) -> None:
    _log("")
    _log(f"{'=' * 60}")
    _log(f"  {title}")
    _log(f"{'=' * 60}")


def _run(
    cmd: list[str],
    *,
    check: bool = True,
    env: dict[str, str] | None = None,
) -> int:
    _log(f"  $ {' '.join(cmd)}")
    merged = {**os.environ, **(env or {})}
    result = subprocess.run(cmd, cwd=ROOT, env=merged)
    if check and result.returncode != 0:
        raise SystemExit(result.returncode)
    return result.returncode


def _python() -> str:
    return sys.executable


def _ensure_repo() -> None:
    if not (ROOT / "api.py").is_file() or not COMPOSE_FILE.is_file():
        _log(f"ERROR: Run this from the Tranc3 repo root (expected {ROOT})")
        raise SystemExit(1)


def _install_deps() -> None:
    _section("Install Python tools")
    _run([_python(), "-m", "pip", "install", "-q", "-r", "requirements.txt"], check=False)
    _run([_python(), "-m", "pip", "install", "-q", *PIP_TOOLS], check=False)


def _infra_mode() -> str:
    sys.path.insert(0, str(ROOT))
    try:
        from src.platform.infrastructure_mode import get_infrastructure_mode

        return get_infrastructure_mode().value
    except Exception:
        raw = os.environ.get("PLATFORM_INFRA_MODE") or os.environ.get(
            "SYSTEM_MODE", "CLOUD_ONLY"
        )
        return str(raw).strip().upper() or "CLOUD_ONLY"


def _generate_env(force: bool, *, local_stack: bool) -> None:
    out = ROOT / ".env.production"
    if out.exists() and not force:
        _log(f"  Using existing {out.name}")
        return

    mode = _infra_mode()
    use_local_db = local_stack or mode == "LOCAL_ONLY"
    _section(f"Generate .env.production (mode={mode}, local_db={use_local_db})")

    def gen_hex() -> str:
        return secrets.token_hex(32)

    def gen_url() -> str:
        return secrets.token_urlsafe(32)

    ts = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    if use_local_db:
        database_url = "sqlite:////app/data/tranc3_platform.db"
