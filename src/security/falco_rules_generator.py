# src/security/falco_rules_generator.py
# Falco runtime security rules generator for the Trancendos platform.
#
# Generates YAML Falco rules from Cryptex threat patterns and built-in
# Tranc3-specific security policies.  Falco is free and open-source (Apache 2.0):
# https://falco.org/
#
# No paid external dependencies. Pure Python — uses stdlib only.
# All methods catch exceptions and return safe defaults; they never raise.

from __future__ import annotations

import logging
import os
import textwrap
from datetime import datetime, timezone
from typing import Any, Dict, List

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# YAML serialisation (stdlib only — no pyyaml required)
# ---------------------------------------------------------------------------


def _yaml_str(value: str) -> str:
    """Minimal YAML scalar quoting for single-line strings."""
    # Quote if string contains special characters
    specials = (
        ":",
        "#",
        "{",
        "}",
        "[",
        "]",
        ",",
        "&",
        "*",
        "?",
        "|",
        "-",
        "<",
        ">",
        "=",
        "!",
        "%",
        "@",
        "`",
        "'",
        '"',
        "\n",
        "\\",
    )
    if any(c in value for c in specials) or not value:
        escaped = value.replace("\\", "\\\\").replace('"', '\\"')
        return f'"{escaped}"'
    return value


def _yaml_list(items: List[str], indent: int = 4) -> str:
    pad = " " * indent
    return "\n".join(f"{pad}- {_yaml_str(item)}" for item in items)


