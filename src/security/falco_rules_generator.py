# Tranc3 Platform — Falco Runtime Security Rules
# Generated by FalcoRulesGenerator at {timestamp}
# Platform: Trancendos / Cryptex security engine
# Falco docs: https://falco.org/docs/rules/
#
# Load with: falco -r /etc/falco/rules.d/tranc3.yaml
