# ---------------------------------------------------------------------------
# SuricataRulesGenerator
# ---------------------------------------------------------------------------


class SuricataRulesGenerator:
    """
    Suricata IDS/IPS rules generator for the Tranc3 / Cryptex platform.

    Generates Suricata/Snort-compatible ``.rules`` files from:
    * Built-in Tranc3 signatures covering SQL injection, XSS, SSRF, JWT
      manipulation, DDoS/flood, malicious user-agents, path traversal,
      command injection, and known CVE payloads (e.g. Log4Shell).
    * Dynamic rules derived from Cryptex ``ThreatSignal`` patterns.
    * Per-CVE rules via ``rule_from_cve()``.

    Usage
    -----
    >>> gen = SuricataRulesGenerator()
    >>> rules_str = gen.generate_default_rules()
    >>> gen.save_rules("/etc/suricata/rules/tranc3.rules")
    >>> cve_rule = gen.rule_from_cve("CVE-2021-44228", "${jndi:")

    All methods catch exceptions and return safe defaults; they never raise.
    """

    def __init__(self) -> None:
        self._generated_rules: str = ""
        self._sid_counter: int = _SID_BASE

    # ── SID management ────────────────────────────────────────────────────

    def _next_sid(self) -> int:
        sid = self._sid_counter
        self._sid_counter += 1
        return sid

    # ── Public API ────────────────────────────────────────────────────────

    def generate_default_rules(self) -> str:
        """
        Return the built-in Tranc3 Suricata rules as a ``.rules`` string.

        Covers:
        - SQL injection (UNION SELECT, OR 1=1, DROP TABLE, stacked queries)
        - XSS (script tags, javascript: URI, event handlers, iframes)
        - SSRF (localhost, 127.0.0.1, AWS metadata, RFC-1918 ranges)
        - JWT manipulation (alg:none, RS256→HS256 confusion)
        - DDoS / flood (HTTP POST flood, SYN flood)
        - Malicious user-agents (sqlmap, Nikto, Masscan, Nmap)
        - Path traversal (../, ..%2F)
        - Command injection (shell metacharacters, backtick substitution)
        - Log4Shell / CVE-2021-44228

        Returns
        -------
        str — Valid Suricata .rules content.
        """
        try:
            self._sid_counter = _SID_BASE
            timestamp = datetime.now(timezone.utc).isoformat()
            lines: List[str] = [_HEADER_TEMPLATE.format(timestamp=timestamp), ""]

            for comment, template in _BUILTIN_RULES:
                sid = self._next_sid()
                meta = _metadata(rev=1, tags=["tranc3", "builtin"])
                rule = template.replace("__META__", meta).replace("__SID__", str(sid))
                lines.append(f"# {comment}")
                lines.append(rule)
                lines.append("")

            self._generated_rules = "\n".join(lines)
            logger.info(
                "suricata: generated %d built-in rules (SID %d–%d)",
                len(_BUILTIN_RULES),
                _SID_BASE,
                self._sid_counter - 1,
            )
            return self._generated_rules
        except Exception as exc:  # noqa: BLE001
            logger.error("suricata: generate_default_rules error: %s", exc)
            return ""

    def generate_rules(self, threat_patterns: List[Dict[str, Any]]) -> str:
        """
        Generate Suricata rules from a list of Cryptex threat pattern dicts.

        Each dict should contain at minimum:
            ``name``        — threat name (str)
            ``description`` — description (str)
            ``severity``    — info/low/medium/high/critical (str)
            ``pattern``     — content pattern to match (str)
            ``direction``   — "request" or "response" (str, optional)

        Built-in rules are always included first.

        Parameters
        ----------
        threat_patterns: List of threat-pattern dicts from Cryptex.

        Returns
        -------
        str — Complete Suricata .rules content (builtins + dynamic rules).
        """
        try:
            base = self.generate_default_rules()
            if not threat_patterns:
                return base

            self._sid_counter = _SID_DYNAMIC_BASE
            dynamic_lines: List[str] = [
                "",
                "# ── Dynamic rules from Cryptex threat patterns ──────────────────────────────",
                "",
            ]
            count = 0
            for idx, pattern in enumerate(threat_patterns):
                try:
                    rule = self._rule_from_pattern(pattern)
                    if rule:
                        dynamic_lines.append(
                            f"# Dynamic[{idx}]: {pattern.get('name', 'unknown')}"
                        )
                        dynamic_lines.append(rule)
                        dynamic_lines.append("")
                        count += 1
                except Exception as inner:  # noqa: BLE001
                    logger.warning("suricata: skipping pattern %d: %s", idx, inner)

            self._generated_rules = base + "\n".join(dynamic_lines)
            logger.info(
                "suricata: appended %d dynamic rule(s) from %d pattern(s)",
                count,
                len(threat_patterns),
            )
            return self._generated_rules
        except Exception as exc:  # noqa: BLE001
            logger.error("suricata: generate_rules error: %s", exc)
            return self._generated_rules or ""

    def rule_from_cve(
        self,
        cve_id: str,
        payload_pattern: Optional[str] = None,
    ) -> str:
        """
        Generate a Suricata rule targeting a specific CVE.

        Parameters
        ----------
        cve_id:          CVE identifier (e.g. "CVE-2021-44228").
        payload_pattern: Optional content string to match in HTTP body/URI.
                         If omitted, a generic probe-detection rule is created.

        Returns
        -------
        str — Single Suricata rule line; empty string on error.
        """
        try:
            sid = _SID_CVE_BASE + abs(hash(cve_id)) % 10000
            meta = _metadata(rev=1, tags=["tranc3", "cve", cve_id.lower()])
            safe_cve = _clean(cve_id)
            safe_msg = f"TRANC3 {safe_cve} exploit attempt"

            if payload_pattern:
                safe_payload = _clean(payload_pattern)
                # Match in both URI and body
                rule = (
                    f'alert http any any -> $HTTP_SERVERS any '
                    f'(msg:"{safe_msg}"; flow:established,to_server; '
                    f'http.request_body; content:"{safe_payload}"; nocase; '
                    f'__META__ sid:__SID__;)'
                )
            else:
                # Generic: alert on any request referencing the CVE ID string
                # (e.g., scanners that include CVE IDs in their probes)
                rule = (
                    f'alert http any any -> $HTTP_SERVERS any '
                    f'(msg:"{safe_msg} generic probe"; flow:established,to_server; '
                    f'http.uri; content:"{safe_cve}"; nocase; '
                    f'__META__ sid:__SID__;)'
                )

            logger.debug("suricata: generated rule for %s (SID %d)", cve_id, sid)
            return rule
        except Exception as exc:  # noqa: BLE001
            logger.error("suricata: rule_from_cve error [%s]: %s", cve_id, exc)
            return ""

    def save_rules(
        self, output_path: str = "/etc/suricata/rules/tranc3.rules"
    ) -> bool:
        """
        Write the most recently generated rules to ``output_path``.

        Generates default rules if none have been produced yet.

        Parameters
        ----------
        output_path: Destination file path (must be writable).

        Returns
        -------
        True on success; False on any error.
        """
        try:
            rules = self._generated_rules or self.generate_default_rules()
            if not rules:
                logger.warning("suricata: no rules to write")
                return False
            dir_path = os.path.dirname(output_path)
            if dir_path:
                os.makedirs(dir_path, exist_ok=True)
            with open(output_path, "w", encoding="utf-8") as fh:
                fh.write(rules)
            logger.info(
                "suricata: rules written to %s (%d bytes)", output_path, len(rules)
            )
            return True
        except Exception as exc:  # noqa: BLE001
            logger.error("suricata: save_rules error [%s]: %s", output_path, exc)
            return False

    # ── Internal helpers ──────────────────────────────────────────────────

    def _rule_from_pattern(self, pattern: Dict[str, Any]) -> str:
        """Build a single Suricata rule from a Cryptex threat pattern dict."""
        name = _clean(pattern.get("name", "Unknown"))
        raw = _clean(pattern.get("pattern", ""))
        severity = pattern.get("severity", "medium").lower()
        tags = pattern.get("tags", ["tranc3", "dynamic"])

        if not raw:
            return ""

        # Map severity to Suricata action
        action = "alert"  # Suricata default — log, don't drop

        sid = self._next_sid()
        meta = _metadata(rev=1, tags=tags)
        msg = f"TRANC3 Dynamic: {name}"

        return (
            f'{action} http any any -> $HTTP_SERVERS any '
            f'(msg:"{msg}"; flow:established,to_server; '
            f'http.request_body; content:"{raw}"; nocase; '
            f'__META__ sid:__SID__;)'
        )
