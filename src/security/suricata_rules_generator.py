# src/security/suricata_rules_generator.py
# Suricata IDS/IPS rules generator for the Trancendos platform.
#
# Generates Suricata/Snort-compatible .rules files from Cryptex threat
# patterns and built-in Tranc3-specific signatures.
# Suricata is free and open-source (GPL-2.0): https://suricata.io/
#
# Output format: valid Suricata rules (SID-based, with metadata).
# No paid external dependencies. Pure Python — stdlib only.
# All methods catch exceptions and return safe defaults; they never raise.

from __future__ import annotations

