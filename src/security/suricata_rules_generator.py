# Tranc3 local SID range: 9000000 – 9099999  (private/local use)
_SID_BASE = 9_000_000
_SID_CVE_BASE = 9_050_000
_SID_DYNAMIC_BASE = 9_080_000


# ---------------------------------------------------------------------------
# Rule builder helpers
# ---------------------------------------------------------------------------


def _clean(text: str) -> str:
    """Remove characters that would break a Suricata rule string option."""
    return text.replace('"', "'").replace(";", ",").replace("\n", " ")


def _metadata(rev: int = 1, tags: Optional[List[str]] = None) -> str:
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d")
    tag_str = ", ".join(tags or ["tranc3"])
    return f'metadata:created_at {ts}, updated_at {ts}, tag {tag_str}; rev:{rev};'


